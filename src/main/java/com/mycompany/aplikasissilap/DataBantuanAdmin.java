    private void refreshTable() {
        try {
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("Nama Bantuan");
            model.addColumn("Jumlah");
            model.addColumn("Deskripsi");
            model.addColumn("Tanggal");

            stat = k.getCon().prepareStatement("SELECT * FROM bantuan");
            rs = stat.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nama_bantuan"),
                    rs.getInt("jumlah"),
                    rs.getString("deskripsi"),
                    rs.getString("tanggal_disalurkan")
                });
            }
            tb_bantuan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error loading table: " + e.getMessage());
        }
    }

    private void pilihData() {
        int row = tb_bantuan.getSelectedRow();
        if (row != -1) {
            selectedId = Integer.parseInt(tb_bantuan.getValueAt(row, 0).toString());
            tf_namabantuan.setText(tb_bantuan.getValueAt(row, 1).toString());
            tf_jumlah.setText(tb_bantuan.getValueAt(row, 2).toString());
            tf_deskripsi.setText(tb_bantuan.getValueAt(row, 3).toString());
            tf_tanggaldisalurkan.setText(tb_bantuan.getValueAt(row, 4).toString());
        }
    }
    
    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data dulu");
            return;
        }
        try {
            stat = k.getCon().prepareStatement("UPDATE bantuan SET nama_bantuan=?, jumlah=?, deskripsi=?, tanggal_disalurkan=? WHERE id=?");
            stat.setString(1, tf_namabantuan.getText());
            stat.setInt(2, Integer.parseInt(tf_jumlah.getText()));
            stat.setString(3, tf_deskripsi.getText());
            stat.setString(4, tf_tanggaldisalurkan.getText());
            stat.setInt(5, selectedId);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error update: " + e.getMessage());
        }
    }//GEN-LAST:event_bt_updateActionPerformed

