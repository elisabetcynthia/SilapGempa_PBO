    private void bt_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cetakActionPerformed
        // TODO add your handling code here:
        try {
            String path = "laporan_bantuan.pdf";
            Document document = new Document();
            PdfWriter.getInstance(document, new FileOutputStream(path));
            document.open();

            Font fontJudul = new Font(Font.FontFamily.TIMES_ROMAN, 18, Font.BOLD);
            Paragraph judul = new Paragraph("Laporan Data Bantuan", fontJudul);
            judul.setAlignment(Element.ALIGN_CENTER);
            document.add(judul);
            document.add(new Paragraph(" ")); // spasi

            PdfPTable pdfTable = new PdfPTable(tb_bantuan.getColumnCount());

            for (int i = 0; i < tb_bantuan.getColumnCount(); i++) {
                pdfTable.addCell(new PdfPCell(new Phrase(tb_bantuan.getColumnName(i))));
            }

            for (int rows = 0; rows < tb_bantuan.getRowCount(); rows++) {
                for (int cols = 0; cols < tb_bantuan.getColumnCount(); cols++) {
                    Object value = tb_bantuan.getValueAt(rows, cols);
                    pdfTable.addCell(value != null ? value.toString() : "");
                }
            }

            document.add(pdfTable);
            document.close();

            JOptionPane.showMessageDialog(this, "Laporan berhasil dicetak ke: " + path);
            Desktop.getDesktop().open(new File(path)); // buka PDF langsung

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak: " + e.getMessage());
        }
    }//GEN-LAST:event_bt_cetakActionPerformed


    
