    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tb_bencana.getSelectedRow();
    if (selectedRow != -1) {
        try {
            int id = (int) model.getValueAt(selectedRow, 0);
            String kejadian = tf_judul.getText();
            String lokasi = tf_lokasi.getText();
            String tanggal = tf_tanggal.getText();
            String status = cb_status.getSelectedItem().toString();

            if (tanggal.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong!");
                return;
            }

            if (!tanggal.matches("\\d{4}-\\d{2}-\\d{2}")) {
                JOptionPane.showMessageDialog(this, "Format tanggal harus YYYY-MM-DD (contoh: 2025-06-28)");
                return;
            }
            
            stat = k.getCon().prepareStatement("UPDATE laporan SET judul=?, lokasi=?, tanggal_lapor=?, status=? WHERE id_user=?");
            stat.setString(1, kejadian);
            stat.setString(2, lokasi);
            stat.setString(3, tanggal);
            stat.setString(4, status);
            stat.setInt(5, id);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update data: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diupdate terlebih dahulu");
    }
    }//GEN-LAST:event_bt_updateActionPerformed

    private void bt_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_deleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tb_bencana.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) model.getValueAt(selectedRow, 0); // Asumsikan kolom ID Pelapor ada di kolom 0
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    stat = k.getCon().prepareStatement("DELETE FROM laporan WHERE id_user = ?");
                    stat.setInt(1, id);
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    refreshTable();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus terlebih dahulu");
        }
    }//GEN-LAST:event_bt_deleteActionPerformed

