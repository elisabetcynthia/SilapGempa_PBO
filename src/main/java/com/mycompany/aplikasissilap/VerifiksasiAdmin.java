    private void refreshTable() {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Kejadian");
        model.addColumn("Lokasi");
        model.addColumn("Tanggal");
        model.addColumn("Status");

        tb_bencana.setModel(model);

        try {
            stat = k.getCon().prepareStatement("SELECT * FROM laporan");
            rs = stat.executeQuery();

            while (rs.next()) {
                Object[] data = {
                    rs.getInt("id_user"),
                    rs.getString("judul"),
                    rs.getString("lokasi"),
                    rs.getString("tanggal_lapor"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error load table: " + e.getMessage());
        }
    }    

    class pelapor extends VerifiksasiAdmin {
        int id_pelapor;
        String kejadian, lokasi, tanggal, status;

        public pelapor () {
            kejadian = tf_judul.getText();
            lokasi = tf_lokasi.getText();
            tanggal = tf_tanggal.getText();    
            status = cb_status.getSelectedItem().toString();
        }
        public void refreshTable(){
            model = new DefaultTableModel();
            model.addColumn("ID Pelapor");
            model.addColumn("Kejadian");
            model.addColumn("Lokasi");
            model.addColumn("Tanggal");
            model.addColumn("Status");
            tb_bencana.setModel(model);
            
            try {
            PreparedStatement stat = k.getCon().prepareStatement("SELECT * FROM laporan");
            ResultSet rs = stat.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getInt("id_pelapor"),
                    rs.getString("judul"),
                    rs.getString("lokasi"),
                    rs.getString("tanggal"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    }

